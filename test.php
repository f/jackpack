<h1>What is Jack?</h1>
<p>
	Jack is a Javascript MVC development toolkit based on jQuery and Underscore.js. It allows you to write Javascript
	applications using MVC structure.
</p>
<h1>What does Jack stands for?</h1>
<p>
	Jack is stands for <b>J</b>avascript <b>A</b>pplication <b>C</b>oding <b>K</b>it
</p>
